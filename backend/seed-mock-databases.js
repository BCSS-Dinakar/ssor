async function seed() {
  console.log('🌱 Seeding 20 CCTNS records and 20 ePetty cases...');

  try {
    // Seed CCTNS
    for (const record of cctnsRecords) {
      await prisma.cctnsRecord.upsert({
        where: { recordId: record.recordId },
        update: record,
        create: record
      });
    }
    console.log(`✅ Seeded ${cctnsRecords.length} CCTNS records.`);

    // Seed ePetty Cases
    for (const ep of ePettyCases) {
      await prisma.ePettyCase.upsert({
        where: { caseNumber: ep.caseNumber },
        update: ep,
        create: ep
      });
    }
    console.log(`✅ Seeded ${ePettyCases.length} ePetty cases.`);

    console.log('🎉 Database seeding completed successfully.');
  } catch (error) {
    console.error('❌ Error during seeding:', error);
  } finally {
    await prisma.$disconnect();
  }
}

seed();
